function labelToHour24($label) {
    $label = trim($label);
    $parts = explode(" ", $label);
    if (count($parts) < 2) return null;
    $h = (int)$parts[0];
    $ampm = strtolower(trim($parts[1]));
    if ($ampm === "pm" && $h !== 12) $h += 12;
    if ($ampm === "am" && $h === 12) $h = 0;
    return $h;
}

$out = [];
foreach ($labels as $lab) {
    $h = labelToHour24($lab);
    if ($h === null) { $out[] = ["predicted" => 0]; continue; }

    // Sum midpoints of all 15-min slots in this hour
    $totalArrivals = 0;
    $slots = $arrivalRates[$location][$day] ?? [];
    foreach ($slots as $slotKey => $range) {
        $slotH = (int)explode(":", $slotKey)[0];
        if ($slotH === $h) {
            $totalArrivals += ($range[0] + $range[1]) / 2;
        }
    }

    // Estimate occupancy: arrivals * (stayMinutes / 60) gives avg people present
    $predicted = round($totalArrivals * ($stayMinutes / 60));
    $out[] = ["predicted" => $predicted];
}

