  [$aMin, $aMax] = getArrivalRangeForNow($arrivalRates, $locName, $day, $now);
  $arrivals = randBetween($aMin, $aMax);

  // capacity enforcement
  $free = $capacity - $current;
  $arrivals = clamp($arrivals, 0, $free);

  if ($arrivals > 0) {
